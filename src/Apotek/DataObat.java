    private void tblObatMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_tblObatMouseClicked
        // TODO add your handling code here:
        btnSimpan.setEnabled(false);
        kodeObat.setEnabled(false);
        btnEdit.setEnabled(true);
        btnHapus.setEnabled(true);
        Object data[] = new Object[5];
        int row = tblObat.getSelectedRow();
        try {
            String sql = "select * from dataobat where kodeObat='"+tableModel.getValueAt(row, 0).toString()+"' ";
            java.sql.Connection conn=(Connection)Koneksi.configDB();
            java.sql.Statement stm=conn.createStatement();
            java.sql.ResultSet rs=stm.executeQuery(sql);
        while (rs.next()) {
            kodeObat.setText(rs.getString("kodeObat"));
            namaObat.setText(rs.getString("namaObat"));
            stok.setText(rs.getString("stok"));
            jenisObat.setSelectedItem(rs.getString("jenisObat"));
            harga.setText(rs.getString("harga"));
            tglKedaluwarsa.setDate(rs.getDate("tglKedaluwarsa"));
        }
        } catch (SQLException ex) {
        System.out.println("ERROR di Queri IsiTable"
                + ex.getMessage());
        }
    }//GEN-LAST:event_tblObatMouseClicked

