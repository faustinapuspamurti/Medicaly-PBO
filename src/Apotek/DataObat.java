    private void btnEditActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnEditActionPerformed
        // TODO add your handling code here:
        try {
        String sql = "UPDATE dataobat SET "
                + "namaObat=?, "
                + "stok=?, "
                + "jenisObat=?, "
                + "harga=?, "
                + "tglKedaluwarsa=? "
                + "WHERE kodeObat=?";
        
        java.sql.Connection conn = (Connection) Koneksi.configDB();
        java.sql.PreparedStatement pst = conn.prepareStatement(sql);

        pst.setString(1, namaObat.getText());
        pst.setString(2, stok.getText());
        pst.setString(3, (String) jenisObat.getSelectedItem());
        pst.setString(4, harga.getText());

        // Periksa apakah tglKedaluwarsa tidak null sebelum mengambil nilainya
        if (tglKedaluwarsa.getDate() != null) {
            java.sql.Date tanggalKedaluwarsa = new java.sql.Date(tglKedaluwarsa.getDate().getTime());
            pst.setDate(5, tanggalKedaluwarsa);
        } else {
            pst.setNull(5, java.sql.Types.DATE);
        }

        pst.setString(6, kodeObat.getText());

        pst.executeUpdate();
        JOptionPane.showMessageDialog(null, "Data Berhasil diubah");
        tampilkanData();
    } catch (SQLException ex) {
        JOptionPane.showMessageDialog(null, "ERROR di Query IsiTable " + ex.getMessage());
    }
    }//GEN-LAST:event_btnEditActionPerformed
    
