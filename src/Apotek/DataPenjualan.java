    public void bersihTable() {
        int row = tblPenjualan.getRowCount() - 1;
        while (row >= 0) {
            tableModel.removeRow(row);
            row--;
        }
    }
    
    public void tampilkanData(){
    tableModel.setRowCount(0);
    bersihTable();
    Object data[] = new Object[6];
    try {
        String sql = "select * from datapenjualan";
        java.sql.Connection conn=(Connection)Koneksi.configDB();
        java.sql.Statement stm=conn.createStatement();
        java.sql.ResultSet rs=stm.executeQuery(sql);
        while (rs.next()) {
            data [0] = rs.getString("kodeObat");
            data [1] = rs.getString("namaObat");
            data [2] = rs.getString("jumlahObat");
            data [3] = rs.getString("harga");
            data [4] = rs.getString("tanggal");
            tableModel.addRow(data);
        }
    } catch (SQLException ex) {
        System.out.println("ERROR di Queri IsiTable" + ex.getMessage());
    }
    
}
