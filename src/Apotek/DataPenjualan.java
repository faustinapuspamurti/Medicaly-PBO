    private void updateStokObat(String kodeObat, int jumlahTerjual) {
        try {
            String sql = "UPDATE dataobat SET stok = stok - ? WHERE kodeObat = ?";
            Connection conn = Koneksi.configDB();
            PreparedStatement pst = conn.prepareStatement(sql);

            pst.setInt(1, jumlahTerjual);
            pst.setString(2, kodeObat);

            pst.executeUpdate();
        } catch (SQLException ex) {
            JOptionPane.showMessageDialog(null, "ERROR dalam mengurangi stok obat: " + ex.getMessage());
        }
    }
