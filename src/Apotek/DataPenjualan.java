    private void kodeObatMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_kodeObatMouseClicked
        // TODO add your handling code here:
        // Saat kode obat diklik, tampilkan data obat yang sesuai
    String kodeObatInput = kodeObat.getText();
    
    // Query untuk mendapatkan data obat berdasarkan kode obat
    String query = "SELECT namaObat, harga FROM dataobat WHERE kodeObat = ?";
    
    try {
        Connection conn = Koneksi.configDB();
        PreparedStatement pst = conn.prepareStatement(query);
        pst.setString(1, kodeObatInput);
        ResultSet rs = pst.executeQuery();
        
        // Jika data obat ditemukan
        if (rs.next()) {
            // Set nilai field namaObat dan harga sesuai dengan data obat yang ditemukan
            namaObat.setText(rs.getString("namaObat"));
            harga.setText(rs.getString("harga"));
        } else {
            // Jika data obat tidak ditemukan, kosongkan field namaObat dan harga
            namaObat.setText("");
            harga.setText("");
        }
    } catch (SQLException ex) {
        ex.printStackTrace();
        JOptionPane.showMessageDialog(this, "Terjadi kesalahan: " + ex.getMessage());
    }
    }//GEN-LAST:event_kodeObatMouseClicked

